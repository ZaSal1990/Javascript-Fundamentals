function lastIndexOf(array,value)
{
  let counter = 0;
  let indexTracker = 0;

  if (array.includes(value))
  {
  for (let item in array)
  {
    if (array[item] === value)
    {counter++;
    indexTracker = item; 
  }
  }
  
return parseInt(indexTracker);
  }

  else return -1;
}






